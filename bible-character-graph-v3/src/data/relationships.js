export const getRelationshipsByCharacter = (characterId) => {
  return relationships.filter(r => r.source === characterId || r.target === characterId);
};

// 두 인물 사이의 관계 찾기
export const getRelationshipBetween = (char1, char2) => {
  return relationships.find(r => 
    (r.source === char1 && r.target === char2) || 
    (r.source === char2 && r.target === char1)
  );
};

